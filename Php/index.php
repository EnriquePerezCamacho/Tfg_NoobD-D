            <h2>Bienvenidos a Dungeons & Dragons</h2>
            <h3>¿Qué es D&D?</h3>
            <p>
                Dungeons & Dragons (D&D) es un juego de rol de mesa que combina narrativa colaborativa, estrategia, creatividad e imaginación.
                Creado por Gary Gygax y Dave Arneson, fue publicado por primera vez en 1974 y rápidamente se convirtió en el referente del género.
                D&D permite a los jugadores sumergirse en mundos llenos de magia, criaturas fantásticas y aventuras épicas, donde la única limitación es su imaginación.
            </p>

            <h3>¿Cómo se juega D&D?</h3>
            <p>
                El juego se desarrolla a través de sesiones en las que los jugadores asumen los roles de héroes y aventureros.
                Estas sesiones son dirigidas por el Dungeon Master (DM), quien actúa como narrador, árbitro y creador del mundo en el que se desarrolla la historia.
            </p>
        </section>

                    <section class="contenido">
            <h3>¿Por qué jugar Dungeons & Dragons?</h3>
                <p>Narración colaborativa, creatividad sin límites, trabajo en equipo y desarrollo de habilidades. Además, D&D es accesible para todos, incluso para principiantes.</p>

            <h2>Componentes Básicos para Empezar</h2>
            <ul>
                <li>Manual del Jugador: Contiene las reglas básicas y guías para crear personajes.</li>
                <li>Dungeon Master Guide y Manual de Monstruos: Opcional para nuevos jugadores.</li>
                <li>Dados poliédricos: Disponibles en sets económicos.</li>
                <li>Hojas de personaje: Plantillas donde los jugadores registran los atributos y estadísticas de su personaje.</li>
                <li>Grupo de jugadores: Idealmente entre 3 y 6 jugadores más un DM.</li>
                <li>Un lugar para jugar: Puede ser una mesa física o una plataforma en línea.</li>
            </ul>
</section>

        <section class="destacado">
            <h2>Elementos Fundamentales</h2>
            <p><strong>El Dungeon Master (DM):</strong><br>
                El DM describe los escenarios, interpreta a los personajes no jugables (PNJs) y crea los retos que enfrentarán los jugadores. No es un oponente, sino una guía que ayuda a tejer una historia conjunta, adaptando el juego según las decisiones de los jugadores.
            </p>
            <p><strong>Los jugadores:</strong><br>
                Cada jugador crea y controla un personaje único con características determinadas como raza (elfo, humano, enano, etc.), clase (guerrero, mago, pícaro, etc.) y trasfondo. A medida que avanza el juego, el personaje gana experiencia, sube de nivel y desbloquea nuevas habilidades.
            </p>
