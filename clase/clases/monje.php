    <h1>Monjes</h1>
    <p>Los monjes están unidos en su capacidad de aprovechar mágicamente la energía que fluye en sus cuerpos. Ya sea canalizado como una sorprendente muestra de destreza en el combate o como un enfoque más sutil de habilidad defensiva y velocidad, esta energía infunde todo lo que hace un monje.</p>
    <p>Debes tener una puntuación de Destreza y una puntuación de Sabiduría de 13 o superior para poder entrar o salir de esta clase en varias clases.</p>

