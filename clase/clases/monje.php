    <div class="section">
        <h2>Características de la Clase</h2>
        <h3>Puntos de Vida</h3>
        <ul>
            <li><strong>Dados de Golpe:</strong> 1d8 por nivel de monje</li>
            <li><strong>Puntos de Golpe en el 1er Nivel:</strong> 8 + tu modificador de Constitución</li>
            <li><strong>Puntos de Golpe en Niveles Superiores:</strong> 1d8 (o 5) + tu modificador de Constitución por nivel de monje después del 1er nivel</li>
        </ul>

        <h3>Competencias</h3>
        <ul>
            <li><strong>Armadura:</strong> Ninguna</li>
            <li><strong>Armas:</strong> Armas simples, espadas cortas</li>
            <li><strong>Herramientas:</strong> Elige un tipo de herramientas de artesano o un instrumento musical</li>
            <li><strong>Tiradas de salvación:</strong> Fuerza, Destreza</li>
            <li><strong>Habilidades:</strong> Elige dos de Acrobacia, Atletismo, Historia, Perspicacia, Religión y Sigilo</li>
        </ul>

        <h3>Equipo</h3>
        <p>Comienzas con el siguiente equipo, además del equipo otorgado por tu trasfondo:</p>
        <ul>
            <li>(a) una espada corta o (b) cualquier arma simple</li>
            <li>(a) un pack de mazmorreo o (b) un pack de explorador</li>
            <li>10 dardos</li>
        </ul>
    </div>

    <div class="section">
        <h2>Defensa sin Armadura</h2>
        <p>A partir del nivel 1, mientras no lleves armadura ni uses un escudo, tu CA es igual a 10 + tu modificador de Destreza + tu modificador de Sabiduría.</p>
    </div>

    <div class="section">
        <h2>Artes Marciales</h2>
        <p>En el nivel 1, tu práctica de las artes marciales te da maestría en estilos de combate que usan golpes desarmados y armas de monje, que son espadas cortas y cualquier arma cuerpo a cuerpo simple que no tenga la propiedad de dos manos o pesada.</p>
        <p>Obtienes los siguientes beneficios mientras estás desarmado o empuñando solo armas de monje y no llevas armadura ni usas un escudo:</p>
        <ul>
            <li>Puedes usar Destreza en lugar de Fuerza para las tiradas de ataque y daño de tus golpes desarmados y armas de monje.</li>
            <li>Puedes tirar un d4 en lugar del daño normal de tu golpe desarmado o arma de monje. Este dado cambia a medida que subes de nivel de monje, como se muestra en la columna de Artes Marciales de la tabla de Monje.</li>
            <li>Cuando usas la acción de Ataque con un golpe desarmado o un arma de monje en tu turno, puedes hacer un golpe desarmado como una acción de bonificación. Por ejemplo, si realizas la acción de Ataque y atacas con un bastón, también puedes hacer un golpe desarmado como una acción de bonificación, asumiendo que no hayas tomado ya una acción de bonificación en este turno.</li>
        </ul>
    </div>

    <div class="section">
        <h2>Ki</h2>
        <p>A partir del nivel 2, tu entrenamiento te permite aprovechar la energía mística del ki. Tu acceso a esta energía está representado por una cantidad de puntos de ki. Tu nivel de monje determina la cantidad de puntos que tienes, como se muestra en la columna de Puntos de Ki de la tabla de Monje.</p>
        <p>Puedes gastar estos puntos para potenciar varias características de ki. Comienzas sabiendo tres características: Ráfaga de golpes, Defensa paciente y Paso del viento. Aprendes más características de ki a medida que subes de nivel en esta clase.</p>
        <p>Cuando gastas un punto de ki, no está disponible hasta que termines un descanso corto o largo, al final del cual recuperas todos los puntos de ki gastados. Debes pasar al menos 30 minutos del descanso meditando para recuperar tus puntos de ki.</p>
        <p>Algunas de tus características de ki requieren que el objetivo haga una tirada de salvación para resistir los efectos de la característica. La CD de la tirada de salvación se calcula de la siguiente manera:</p>
        <p><strong>CD de salvación de ki = 8 + tu bonificación de competencia + tu modificador de Sabiduría</strong></p>

        <h3>Ráfaga de Golpes</h3>
        <p>Inmediatamente después de realizar la acción de Ataque en tu turno, puedes gastar 1 punto de ki para hacer dos golpes desarmados como una acción de bonificación.</p>

        <h3>Defensa Paciente</h3>
        <p>Puedes gastar 1 punto de ki para realizar la acción de Esquivar como una acción de bonificación en tu turno.</p>

        <h3>Paso del Viento</h3>
        <p>Puedes gastar 1 punto de ki para realizar la acción de Desenganchar o Correr como una acción de bonificación en tu turno, y tu distancia de salto se duplica durante este turno.</p>
    </div>

    <div class="section">
        <h2>Movimiento sin Armadura</h2>
        <p>A partir del nivel 2, tu velocidad aumenta en 10 pies mientras no lleves armadura ni uses un escudo. Este aumento se incrementa cuando alcanzas ciertos niveles de monje, como se muestra en la tabla de Monje.</p>
        <p>En el nivel 9, obtienes la capacidad de moverte a lo largo de superficies verticales y cruzar líquidos en tu turno sin caer durante el movimiento.</p>
    </div>

    <div class="section">
        <h2>Arma Dedicada (Opcional)</h2>
        <p>También en el nivel 2, te entrenas para usar una variedad de armas como armas de monje, no solo armas cuerpo a cuerpo simples y espadas cortas. Cada vez que termines un descanso corto o largo, puedes tocar un arma, enfocar tu ki en ella y luego contar esa arma como un arma de monje hasta que uses esta característica de nuevo.</p>
        <p>El arma elegida debe cumplir con estos criterios:</p>
        <ul>
            <li>El arma debe ser un arma simple o marcial.</li>
            <li>Debes ser competente con ella.</li>
            <li>No debe tener las propiedades pesada o especial.</li>
        </ul>
    </div>

    <div class="section">
        <h2>Tradición Monástica</h2>
        <p>Cuando alcanzas el nivel 3, te comprometes con una tradición monástica. Tu tradición te otorga características en el nivel 3 y nuevamente en los niveles 6, 11 y 17.</p>

        <h3>Tradiciones</h3>
        <ul>
            <li><strong>Ser Astral:</strong> El Caldero de Todo de Tasha</li>
            <li><strong>Dragón Ascendente:</strong> El Tesoro de Fizban de los Dragones</li>
            <li><strong>Maestro Borracho:</strong> Guía de Xanathar para Todo</li>
            <li><strong>Cuatro Elementos:</strong> Manual del Jugador</li>
            <li><strong>Kensei:</strong> Guía de Xanathar para Todo</li>
            <li><strong>Muerte Larga:</strong> Guía del Aventurero de la Costa de la Espada</li>
            <li><strong>Misericordia:</strong> El Caldero de Todo de Tasha</li>
            <li><strong>Mano Abierta:</strong> Manual del Jugador</li>
            <li><strong>Sombra:</strong> Manual del Jugador</li>
            <li><strong>Alma Solar:</strong> Guía del Aventurero de la Costa de la Espada</li>
        </ul>
    </div>

